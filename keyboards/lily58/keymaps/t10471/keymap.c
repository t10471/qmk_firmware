int KANA_STATUS = EISU;

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case EISU:
      if (record->event.pressed) {
        if(keymap_config.swap_lalt_lgui==false){
          register_code(KC_LANG2);
        }else{
          SEND_STRING(SS_LALT("`"));
        }
      } else {
        unregister_code(KC_LANG2);
      }
      return false;
      break;
    case KANA:
      if (record->event.pressed) {
        if(keymap_config.swap_lalt_lgui==false){
          register_code(KC_LANG1);
        }else{
          SEND_STRING(SS_LALT("`"));
        }
      } else {
        unregister_code(KC_LANG1);
      }
      return false;
      break;
    case KANAT:
      if (record->event.pressed) {
        if(keymap_config.swap_lalt_lgui==false){
          if(KANA_STATUS == EISU) {
            register_code(KC_LANG1);
          } else {
            register_code(KC_LANG2);
          }
        }else{
          SEND_STRING(SS_LALT("`"));
        }
      } else {
        if(KANA_STATUS == EISU) {
          unregister_code(KC_LANG1);
        } else {
          unregister_code(KC_LANG2);
        }
        if(KANA_STATUS == EISU) {
          KANA_STATUS = KANA;
        } else {
          KANA_STATUS = EISU;
        }
      }
      return false;
      break;
  }
  return true;
}
